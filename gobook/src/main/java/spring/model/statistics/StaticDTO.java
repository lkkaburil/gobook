package spring.model.statistics;

public class StaticDTO {
	private int stat_num;
	private String stat_ip;
	private String stat_date;
	private String s_id;
	private String c_id;
	
	public int getStat_num() {
		return stat_num;
	}
	public void setStat_num(int stat_num) {
		this.stat_num = stat_num;
	}
	public String getStat_ip() {
		return stat_ip;
	}
	public void setStat_ip(String stat_ip) {
		this.stat_ip = stat_ip;
	}
	public String getStat_date() {
		return stat_date;
	}
	public void setStat_date(String stat_date) {
		this.stat_date = stat_date;
	}
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
}
