package spring.model.message;

public class MessageDTO {
	private int m_num;
	private String m_content;
	private String m_wdate;
	private String m_sendid;
	private String m_receiveid;
	private String m_readcheck;
	
	public int getM_num() {
		return m_num;
	}
	public void setM_num(int m_num) {
		this.m_num = m_num;
	}
	public String getM_content() {
		return m_content;
	}
	public void setM_content(String m_content) {
		this.m_content = m_content;
	}
	public String getM_wdate() {
		return m_wdate;
	}
	public void setM_wdate(String m_wdate) {
		this.m_wdate = m_wdate;
	}
	public String getM_sendid() {
		return m_sendid;
	}
	public void setM_sendid(String m_sendid) {
		this.m_sendid = m_sendid;
	}
	public String getM_receiveid() {
		return m_receiveid;
	}
	public void setM_receiveid(String m_receiveid) {
		this.m_receiveid = m_receiveid;
	}
	public String getM_readcheck() {
		return m_readcheck;
	}
	public void setM_readcheck(String m_readcheck) {
		this.m_readcheck = m_readcheck;
	}
	
	
	
}
